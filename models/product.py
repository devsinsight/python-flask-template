class Product:
	def __init__(self, id, name, code, release_date, description, price, star_rating, image_url):
		self.id = id
		self.name = name
		self.code = code
		self.release_date = release_date
		self.description = description
		self.price = price
		self.star_rating = star_rating
		self.image_url = image_url
